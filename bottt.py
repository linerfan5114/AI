# دستور /start
@bot.message_handler(commands=['start'])
def start_message(message):
    markup = InlineKeyboardMarkup(row_width=2)
    btn1 = InlineKeyboardButton("📝 نام‌های پیشنهادی قبلی", callback_data='prev')
    btn2 = InlineKeyboardButton("✨ نام‌های جدید (بیشتر)", callback_data='new')
    btn3 = InlineKeyboardButton("ℹ️ درباره Arena Team", callback_data='about')
    markup.add(btn1, btn2, btn3)
    
    bot.send_message(message.chat.id, INTRO, parse_mode='Markdown', reply_markup=markup)

# هندل کردن دکمه‌ها
